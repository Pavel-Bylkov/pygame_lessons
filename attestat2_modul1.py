def summ(x, y):
    """Возвращают сумму двух переданных аргументов"""
    return x + y

def sub(x, y):
    """Возвращают разницу двух переданных аргументов"""
    return x - y

def multi(x, y):
    """Возвращают произведение двух переданных аргументов"""
    return x * y

def div(x, y):
    """Возвращают частное от деления x на y"""
    return x / y